#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i1;
	int		i2;

	i1 = 0;
	i2 = 0;
	if (s != NULL && f != NULL)
	{
		i1 = ft_strlen(s);
		while (i2 < i1)
		{
			(*f)(i2, s);
			s++;
			i2++;
		}
	}
}
